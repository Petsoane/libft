#include "libft.h"

void	ft_putnbr(int num)
{
	if (num < 0)
	{
		ft_putchar('-');
		num = -num;
	}
	if (num < 10)
	{
		ft_putchar((num % 10) + '0');
		return;
	}
	ft_putnbr(num / 10);
	ft_putchar((num % 10) + '0');
}
