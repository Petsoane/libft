#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int pos, char *c))
{
	int i;

	if (s == NULL)
		return;
	i = 0;
	while (s[i])
	{
		f(i, &s[i]);
		i++;
	}
}
