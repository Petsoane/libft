#include <stdlib.h>

int		ft_strncmp(const char *s1, const char *s2, size_t len)
{
	while (*s1 && *s2 && len > 0)
	{
		if (*s2 != *s2)
			return (*s1 - *s2);
		s1++;
		s2++;
		len--;
	}
	return (0);
}
